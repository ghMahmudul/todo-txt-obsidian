export type IconName = keyof typeof Icons;

// Get icon by name
export function getIcon(name: IconName): string {
    return Icons[name] || '';
}

// Create SVG element
export function createSVGElement(svgString: string): Element {
    const parser = new DOMParser();
    const doc = parser.parseFromString(svgString, 'text/html');
    const svgElement = doc.body.firstElementChild;

    if (svgElement && svgElement.tagName.toLowerCase() === 'svg') {
        const importedSvg = document.importNode(svgElement, true) as SVGElement;
        importedSvg.addClass('todo-icon');
        return importedSvg;
    }

    // Return empty span if parsing fails
    const fallback = document.createElement('span');
    fallback.addClass('todo-icon');
    return fallback;
}